class VaultLoadingSkeleton extends StatelessWidget {
  const VaultLoadingSkeleton({super.key});

  @override
  Widget build(BuildContext context) {
    final isDark = Theme.of(context).brightness == Brightness.dark;

    return Shimmer.fromColors(
      baseColor: isDark ? AppColors.darkCard : const Color(0xFFEEF1F8),
      highlightColor: isDark ? AppColors.darkBorder : const Color(0xFFF8FAFF),
      child: ListView.builder(
        padding: const EdgeInsets.fromLTRB(20, 4, 20, 0),
        itemCount: 6,
        itemBuilder: (_, __) => const _SkeletonCard(),
      ),
    );
  }
}

